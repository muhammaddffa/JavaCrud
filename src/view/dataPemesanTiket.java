    public void loadData() {
        try {
            model.setRowCount(0);
            Connection c = Connect.MySQL();
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tiket");
            int no = 0;

            while (rs.next()) {
                no++;
                String data_nama = rs.getString("Nama");
                String data_Kursi = rs.getString("Nomor_Kursi");
                String data_Tiket = rs.getString("Jumlah_Tiket");
                String data_Bayar = rs.getString("Total_Bayar");
                String data_Uang = rs.getString("Uang_Bayar");
                String data_Kembalian = rs.getString("Uang_Kembalian");
                String data_ShowCase = rs.getString("ShowCase");
                String data_Harga = rs.getString("Harga");

//                System.out.println(data_city);
                Object[] data = {no, data_nama, data_Kursi, data_Tiket, data_Bayar, data_Uang, data_Kembalian, data_ShowCase, data_Harga};
                int jumlah = data.length - 1;
                model.addRow(data);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

