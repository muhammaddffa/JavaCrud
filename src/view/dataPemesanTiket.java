    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here: 
        int selectedRowIndex = tableData.getSelectedRow();
        String selectedId = tableData.getValueAt(selectedRowIndex, 0).toString();
        try {
            String sql = "UPDATE tiket SET Showcase = ?, Harga = ?, Nama = ?, Jumlah_Tiket = ?, Nomor_Kursi = ?, Total_Bayar = ?, Uang_Bayar = ?, Uang_Kembalian = ? WHERE id = ?";
            java.sql.Connection conn;
            conn = (Connection) Connect.MySQL();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);

            pstm.setString(1, jns.getSelectedItem().toString());
            pstm.setString(2, hrgField.getText());
            pstm.setString(3, FieldName.getText());
            pstm.setString(4, jmlTiket.getText());
            pstm.setString(5, nmrKursi.getText());
            pstm.setString(6, ttlBayar.getText());
            pstm.setString(7, uangByr.getText());
            pstm.setString(8, uangKmblian.getText());
            pstm.setString(9, selectedId);

            pstm.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            loadData();
            clearInputFields();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_btnEditActionPerformed

